#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*out;
	int		i;

	if (!s1)
		return (NULL);
	i = 0;
	if (!(out = (char *)malloc(sizeof(char) * (ft_strlen(s1) + 1))) || !s1)
		return (NULL);
	while (s1[i])
	{
		out[i] = s1[i];
		i++;
	}
	out[i] = '\0';
	return (out);
}
